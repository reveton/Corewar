#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar(45);
		if (n == -2147483648)
		{
			ft_putstr("2147483648");
			return ;
		}
		n = -n;
	}
	if (n >= 0 && n < 10)
	{
		ft_putchar(n + 48);
		return ;
	}
	else
	{
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
			return ;
		}
	}
}
