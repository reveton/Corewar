#include "../include/libft.h"

int		ft_isalnum(int c)
{
	unsigned char s;

	s = (unsigned char)c;
	if (c < 0 || c > 255)
		return (0);
	if ((s >= '0' && s <= '9') || (s >= 'A' && s <= 'Z') ||
		(s >= 'a' && s <= 'z'))
		return (1);
	return (0);
}
