#include "../include/libft.h"

int		ft_tolower(int c)
{
	unsigned char s;

	s = (unsigned char)c;
	if (c < 0 || c > 255)
		return (c);
	if (s >= 65 && s <= 90)
		s = s + 32;
	return (s);
}
