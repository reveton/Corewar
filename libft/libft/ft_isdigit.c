#include "../include/libft.h"

int		ft_isdigit(int c)
{
	unsigned char s;

	s = (unsigned char)c;
	if (c < 0 || c > 255)
		return (0);
	if (s >= 48 && s <= 57)
		return (1);
	return (0);
}
